                <h1 class="display-4 fw-medium mb-4 fs-1" id="markhor-in-kashmir">
                The Markhor in Kashmir: Ecology, Conservation, and Cultural Significance
                </h1>
                <p class=" mb-4">
                The Kashmir markhor ( Capra falconeri ), a majestic wild goat species, is native to the mountainous regions of Kashmir and northern Pakistan. Recognizable by its spiraled horns and rugged appearance, it inhabits steep, rocky terrains of the Himalayas and Hindu Kush ranges. The species is classified as Near Threatened on the IUCN Red List, with populations fragmented due to habitat loss, poaching, and competition with livestock.
                </p>
                <img src="../assets/img/markhor.webp" alt="Blog image" class="img-fluid mb-4 w-100 object-fit-cover rounded-4"style="aspect-ratio:16/9"/>
                <h4 class="fw-medium mb-4" id="traditional-and-medical-signifcance">
                Traditional and Medicinal Significance
                </h4>
                <h5 class="fw-medium mb-4">Ethnomedicinal Uses</h5>
                <p>In tribal communities of the Thakht-e-Sulaiman Hills (northwest Pakistan), the markhor has historically been hunted for its bile, meat, and skin.</p>
                <ul class="mb-5">
                    <li class="mb-3 fs-6"><strong class=" mb-3 fs-5">Bile:</strong> Used to treat hepatitis and liver disorders.</li>
                    <li class="mb-3 fs-6"><strong class=" mb-3 fs-5">Skin:</strong> Worn as a medicinal remedy for ailments like arthritis and respiratory issues</li>
                    <li class="mb-3 fs-6"><strong class="mb-3 fs-5">Horns:</strong>Sold as decorative items and trophies, contributing to illegal wildlife trade.</li>
                   
                </ul>
                <h5 class="fw-medium mb-4">Cultural Practices</h5>
                <p>Pashtun tribes and other local communities integrate markhor-derived products into traditional healing rituals, often passed down orally.</p>

                <h4 class="fw-medium mb-4" id="conservation-challenges">
                Conservation Challenges
                </h4>
                <ul class="mb-4 ">
                    <li class="mb-3 fs-6"><strong class=" mb-3 fs-5">Poaching and Illegal Trade:</strong> High demand for markhor horns and bile in black markets drives unsustainable hunting.</li>
                    <li class="mb-3 fs-6"><strong class="mb-3 fs-5">Habitat Fragmentation:</strong> Encroachment by livestock, deforestation, and infrastructure development reduce viable habitats.</li>
                    <li class="mb-3 fs-6"><strong class="mb-3 fs-5">Human-Wildlife Conflict:</strong> Competition for grazing land exacerbates tensions between markhor populations and local herders.</li>
                 
                </ul>
                <h4 class="fw-medium mb-4" id="conservation-efforts">
                Conservation Efforts
                </h4>
                <ul class="mb-4 ">
                    <li class="mb-3 fs-6"><strong class=" mb-3 fs-5">Protected Areas:</strong> The Kashmir markhor is found in protected regions like the Chitral Gol National Park and the Sulaiman Mountains.</li>
                    <li class="mb-3 fs-6"><strong class="mb-3 fs-5">Community Engagement:</strong> Initiatives to involve local tribes in conservation, such as ecotourism and sustainable resource management, aim to reduce poaching.</li>
                    <li class="mb-3 fs-6"><strong class="mb-3 fs-5">Legal Frameworks: </strong> Listed under Appendix I of CITES, prohibiting international trade. Pakistan’s Wildlife Act also enforces strict penalties for illegal hunting.
                    </li>
                 
                </ul>
               
                
